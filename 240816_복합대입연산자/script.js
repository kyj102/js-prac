// num1에는 10을 넣고, num2에는 3을 넣어서 대입 연산자 방식으로 num1에 num2를+, -, *(곱하기), /(나누기), %(나머지) 하고 결과를 num1에 넣어서 num1을 출력해주세요.

let num1 = 10;
const num2 = 3;

num1 += num2;
console.log(num1);

num1 -= num2;
console.log(num1);

num1 *= num2;
console.log(num1);

num1 /= num2;
console.log(num1);

num1 %= num2;
console.log(num1);